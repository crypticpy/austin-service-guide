export type ResultsT = (
  key: string,
  vars?: Record<string, string | number>,
) => string;

export function makeResultsT(lang: string | undefined | null): ResultsT {
  const dict = (lang && RESULTS_I18N[lang]) || RESULTS_I18N.en;
  const fallback = RESULTS_I18N.en;
  return (key, vars) => {
    let str = dict[key] ?? fallback[key] ?? key;
    if (vars) {
      // {n} → vars.n
      // {s} → "s" if n !== 1 else "" (English-style pluralization). Other
      // languages don't use this — they should put the literal noun in the
      // template string.
      const n = vars.n;
      if (typeof n === "number") {
        str = str.replace("{n}", String(n)).replace("{s}", n === 1 ? "" : "s");
      }
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(`{${k}}`, String(v));
      }
    }
    return str;
  };
}
